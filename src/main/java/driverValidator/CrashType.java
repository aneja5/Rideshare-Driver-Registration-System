package option2;

/**
 * Enumerates types of crashes.
 */
public enum CrashType {
    /**
     * Represents a fender bender, a minor collision.
     */
    FENDER_BENDER,

    /**
     * Represents a crash without injuries.
     */
    CRASH_WITHOUT_INJURIES,

    /**
     * Represents a crash involving injuries.
     */
    CRASH_INVOLVING_INJURIES
}
